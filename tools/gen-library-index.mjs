#!/usr/bin/env node
/**
 * Regenerate src/data/library-index.json from the BSI Stand-der-Technik-Bibliothek (ADR-0005).
 *
 * Walks the upstream repo via the GitHub Contents API (which returns each file's `sha` + `size`)
 * and records catalogs + component-definitions with a category derived from the top-level folder.
 * Run manually when tracking upstream additions — NOT part of CI or the test suite (it needs
 * network + is rate-limited to 60 req/h unauthenticated; pass a token via GITHUB_TOKEN to raise it).
 *
 *   node tools/gen-library-index.mjs > src/data/library-index.json
 *
 * Decision IDs: ADR-0005.
 */
const OWNER = 'BSI-Bund';
const REPO = 'Stand-der-Technik-Bibliothek';
const REF = 'main';
const API = `https://api.github.com/repos/${OWNER}/${REPO}/contents`;

const CATEGORY_BY_TOP = {
  Anwenderkataloge: 'Anwenderkatalog',
  Quellkataloge: 'Quellkatalog',
  Implementierungsbeschreibungen: 'Komponente',
};

function artifactTypeFor(path) {
  if (path.endsWith('-catalog.json')) return 'catalog';
  if (path.endsWith('-component_definition.json')) return 'component-definition';
  return null; // ignore docs and other files
}

function titleFrom(path) {
  return path
    .split('/')
    .pop()
    .replace(/-(catalog|component_definition)\.json$/, '')
    .replace(/-/g, ' ');
}

async function ghJson(url) {
  const headers = { Accept: 'application/vnd.github+json' };
  if (process.env.GITHUB_TOKEN) headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
  const res = await fetch(url, { headers });
  if (!res.ok) throw new Error(`GitHub API ${res.status} for ${url}`);
  return res.json();
}

async function walk(path = '') {
  const entries = await ghJson(`${API}/${path}?ref=${REF}`);
  const out = [];
  for (const e of entries) {
    if (e.type === 'dir') {
      out.push(...(await walk(e.path)));
    } else if (e.type === 'file') {
      const artifactType = artifactTypeFor(e.path);
      if (!artifactType) continue;
      const top = e.path.split('/')[0];
      out.push({
        path: e.path,
        artifactType,
        title: titleFrom(e.path),
        category: CATEGORY_BY_TOP[top] ?? 'Komponente',
        sha: e.sha,
        size: e.size,
      });
    }
  }
  return out;
}

const entries = (await walk()).sort((a, b) => a.path.localeCompare(b.path));
const manifest = {
  generatedAt: new Date().toISOString(),
  source: `https://github.com/${OWNER}/${REPO}`,
  ref: REF,
  license: 'CC-BY-SA-4.0',
  note: 'Generated by tools/gen-library-index.mjs from the GitHub Contents API; sha/size pin a known-good set. Regenerate to track upstream.',
  entries,
};
process.stdout.write(JSON.stringify(manifest, null, 2) + '\n');
